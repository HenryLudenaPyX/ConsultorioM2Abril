    private List<Medicament> Cards;
    private List<Medicament> Cards(){
        List<Medicament> medicaments = new ArrayList<>();
        String query = "SELECT id, name, stock FROM medicament WHERE stock <= 5 AND status = 'activo'";

        try (Connection con = DBConnection.connection();
             PreparedStatement pstmt = con.prepareStatement(query);
             ResultSet rs = pstmt.executeQuery()) {

            while (rs.next()) {
                Medicament medicament = new Medicament();
                medicament.setId(rs.getInt("id"));
                medicament.setName(rs.getString("name"));
                medicament.setStock(rs.getInt("stock"));
                medicaments.add(medicament);
            }
        } catch (SQLException e) {
            System.out.println("Error in listMedicamentStock : " + e.getMessage());
            e.printStackTrace();
        }
        return medicaments;
    }
    private void initializeComponentsStock(){
        Cards = new ArrayList<>(Cards());
        try {
            for (int i = 0; i < Cards.size(); i++) {
                FXMLLoader fxmlLoader = new FXMLLoader();
                fxmlLoader.setLocation(getClass().getResource("/com/prototype/cm2a/components/stock-notification.fxml"));
                Pane cardBox = fxmlLoader.load();
                StockNotification stockNotification = fxmlLoader.getController();
                stockNotification.setDataStockNotification(Cards.get(i));
                vBoxNotiStock.getChildren().add(cardBox);
            }
        }catch (IOException e){
            System.out.println("Error in initializeComponents : " + e.getMessage());
            e.printStackTrace();
        }
    }

    private List<Medicament> CardsNoti;
    private List<Medicament> CardsNoti(){
        List<Medicament> medicaments = new ArrayList<>();
        String query = "SELECT id, name, expirationdate FROM medicament WHERE expirationdate <= CURRENT_DATE + INTERVAL '6 months' AND status = 'activo'";

        try (Connection con = DBConnection.connection();
             PreparedStatement pstmt = con.prepareStatement(query);
             ResultSet rs = pstmt.executeQuery()) {
            while (rs.next()) {
                Medicament medicament = new Medicament();
                medicament.setId(rs.getInt("id"));
                medicament.setName(rs.getString("name"));
                medicament.setExpirationDate(rs.getString("expirationdate"));
                medicaments.add(medicament);
            }
        } catch (SQLException e) {
            System.out.println("Error in listMedicamentStock : " + e.getMessage());
            e.printStackTrace();
        }
        return medicaments;
    }
